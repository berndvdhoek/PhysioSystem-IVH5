    private void setComboBox() throws RemoteException{
        ArrayList<Employee> therapists = controller.loadTherapists();
        for (Employee e: therapists) {
            therapistComboBox.addItem(new ComboItem(e.getLastname(), e.getID()));
        }
    }
    
    class ComboItem {
        private String key, value;

        public ComboItem(String key, String value) {
            this.key = key;
            this.value = value;
        }
        
        @Override
        public String toString() {
            return key;
        }
        
        public String getKey() {
            return key;
        }
        
        public String getValue() {
            return value;
        }
    }

